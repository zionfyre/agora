/**
 * Build the full system prompt for a voice in a given round.
 * Re-injects ontological grounding and blind spots every round
 * to prevent prompt drift (per spec engineering note).
 */
export function buildVoicePrompt(
  voice: VoiceDefinition,
  roundInstructions: string
): string {
  // Extract Layer 1 and Layer 4 for re-injection
  const grounding = voice.systemPrompt.match(
    /## LAYER 1: ONTOLOGICAL GROUNDING\n([\s\S]*?)(?=\n## LAYER 2)/
  )?.[1] ?? "";
  const blindSpots = voice.systemPrompt.match(
    /## LAYER 4: EXPLICIT BLIND SPOTS\n([\s\S]*?)$/
  )?.[1] ?? "";

  return `${voice.systemPrompt}

---

# ROUND INSTRUCTIONS
${roundInstructions}

# TILT REINFORCEMENT (re-injected every round to prevent drift)
Remember your ontological grounding:
${grounding.trim()}

Remember your blind spots:
${blindSpots.trim()}`;
}
